package com.example.javatutorials.array;

public class LengthDemo {
    public static void main(String[] args) {
        String[] classGroup = new String[5];
        classGroup[0] = "Theo";
        System.out.println(classGroup.length);
        classGroup[1] = "Rafin";
        System.out.println(classGroup.length);
        classGroup[2] = "Jeffery";
        System.out.println(classGroup.length);
        classGroup[3] = "Azaria";
        System.out.println(classGroup.length);
        classGroup[4] = "Daniel";
        System.out.println(classGroup.length);

    }
}
